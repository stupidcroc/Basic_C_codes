#include <stdio.h>

int main()
{
    /*Program to print primitive datatypes
    Datatypes--->1]Primitive datatypes
    2]Derived datatypes
    3]User defined datatypes 
    */
    int num1 = 25;
    char ch = 'A';
    float num2 = 25.24;
    double num3 = 25.254;
    printf("num = %d \n", num1);
    printf("char = %c \n", ch);
    printf("float = %f \n", num2);
    printf("double = %lf \n", num3);
    return 0;
}